  echo "[тЪая╕П] р╣Др╕бр╣Ир╕Юр╕Ъ pnpm р╣Вр╕Ыр╕гр╕Фр╕Хр╕┤р╕Фр╕Хр╕▒р╣Йр╕З pnpm р╕Бр╣Ир╕нр╕Щр╣Гр╕Кр╣Йр╕Зр╕▓р╕Щ ts-prune" >> "$REPORT_FILE"
