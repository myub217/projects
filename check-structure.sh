  declare -A imports=()

  while IFS= read -r line; do
    if [[ $line =~ ^import[[:space:]]+([A-Za-z0-9_]+)[[:space:]]+from[[:space:]]+[\'\"](.+)[\'\"] ]]; then
      imports["${BASH_REMATCH[1]}"]="${BASH_REMATCH[2]}"
    fi
