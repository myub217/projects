          if ($i ~ /^path=/) {
            gsub(/path=|"|'\''/, "", $i);
            path = $i;
          }
          if ($i ~ /^element=</) {
            match($i, /element={<([^ >]+)/, arr);
            if (arr[1] != "") {
              component = arr[1];
            }
          }
        }
        print path, component;
      }
    }
  ' "$ROUTER" | while IFS= read -r route comp; do
    echo "### ЁЯФ╕ Route: /$route" >> "$REPORT_FILE"
    echo "- Component: \`$comp\`" >> "$REPORT_FILE"
