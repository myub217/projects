echo -e "\n## ЁЯз╣ ts-prune (exports р╕Чр╕╡р╣Ир╣Др╕бр╣Ир╣Др╕Фр╣Йр╣Гр╕Кр╣Йр╕Зр╕▓р╕Щ)" >> "$REPORT_FILE"
