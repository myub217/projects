  echo "[тЪая╕П] р╣Др╕бр╣Ир╕Юр╕Ър╕Др╕│р╕кр╕▒р╣Ир╕З tree тАФ р╕Хр╕┤р╕Фр╕Хр╕▒р╣Йр╕Зр╕Фр╣Йр╕зр╕в \`pkg install tree\` р╕лр╕гр╕╖р╕н \`brew install tree\`" >> "$REPORT_FILE"
